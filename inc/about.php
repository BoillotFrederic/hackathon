    <h4 class="titreabout">Chelfies, a Simplon Student's Idea.</h4>
    <p>C'est en effet à l'approche des fêtes de fin d'année 2016 que 4 étudiants de la grande école du numérique "Simplon" que Frédéric Boillot, Elise Daudenthun, Christian Vidal et François Andrzejewski ont eu l'idée de créer ce site.</p>
    <p>L'idée de base était de proposer aux utilisateurs une approche plus divertissante des monuments aux alentours de Boulogne. Avec le succès que connait les selfies, la création d'un tel concours nous semble cohérent sur l'idée de faire découvrir de façon amusante le patrimoine du boulonnais.</p>
    <p>De plus ce projet profite aux petits commerces du boulonnais car les participants qui seront séléctionnés chaque semaine gagneront des coupons de réductions à valoir dans ces commerces.</p>
    <p>Toute l'équipe tient à remercier les participants et visiteurs de notre site!</p>
